export function getProductImages(name: string): string[] {
  const nameLower = name.toLowerCase()
  
  // Check for specific product models first
  for (const [category, models] of Object.entries(productImages)) {
    if (nameLower.includes(category)) {
      // Check for specific model
      for (const [model, images] of Object.entries(models)) {
        if (model !== 'default' && nameLower.includes(model)) {
          return images
        }
      }
      // Return default images for category
      return (models as any).default || (models as any)[Object.keys(models)[0]]
    }
  }
  
  // Fallback to generic product image
  return [
    'https://images.unsplash.com/photo-1523275335684-37898b6baf30?w=800',
    'https://images.unsplash.com/photo-1505740420928-5e560c06d30e?w=800',
    'https://images.unsplash.com/photo-1526170375885-4d8ecf77b99f?w=800'
  ]
}
